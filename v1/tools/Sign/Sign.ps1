param(
	$Path = $(throw "USAGE: Sign -Path <path>")
)
process
{
	# get the script directory
	$invocation = (get-variable MyInvocation -scope 0).Value
	$pfxpath = join-path (split-path $invocation.MyCommand.Path) KeyPair.pfx

	$cert = Get-PfxCertificate $pfxpath
	Set-AuthenticodeSignature $Path $cert -includeChain All -timeStampServer 'http://timestamp.verisign.com/scripts/timstamp.dll'
}

